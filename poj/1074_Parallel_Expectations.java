import java.util.Scanner;

class Main {

  public static void main(String args[]) {
    Scanner in = new Scanner(System.in);
    int t = in.nextInt();
    while (t-- > 0) {
      while (true) {
        String line = in.nextLine();
        if (line.equals("END")) 
          break;
      }

      while (true) {
        String line = in.nextLine();
        if (line.equals("END")) 
          break;
      }
    }
  }

}
